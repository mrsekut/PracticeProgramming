// 型
// ============================
/* 文字型(シングルクォート) */
val c: Char = 'a'

/* 文字列型(ダブルクオート) */
val msg: String = "hello world"

/* 整数型 */
// Byte, Short, Int, Long
val i: Int = 100
val l: Long = 5555555L // 末尾に`L`をつける(大文字のみ)

/* 浮動小数点数 */
// Float, Double
val d: Double  = 234.44
val f: Float = 12.344F // 末尾に`F`をつける(小文字可)

/* 論理型 */
// Boolean
val flg: Boolean = true

